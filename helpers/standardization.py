def inverse_transform_asinh(y_hat: np.ndarray, residuals: np.ndarray, 
                             center: float, scale: float) -> np.ndarray:
    """
    Bias-corrected inverse asinh transformation (Narajewski & Ziel, 2020).
    
    y_hat     : predicted values in transformed space, shape (n,)
    residuals : in-sample residuals, shape (D,)
    center    : μ from median standardization
    scale     : σ from median standardization
    """
    arg = np.clip(y_hat[:, None] + residuals[None, :], -500, 500) # Clipping required to avoid overflow
    return np.sinh(arg).mean(axis=1) * scale + center


