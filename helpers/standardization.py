def reshape_forecast(df):
    """
    Reshape the dfs back to the original date-format where each row is an hour.
    Columns are the point forecasts for 6m, 12m, 18m and 24m calibration windows
    """
    long = df.stack().reset_index()
    long.columns = ['date', 'col', 'value']
    long['hour'] = long['col'].str.extract(r'h(\d+)_').astype(int)
    long['window'] = long['col'].str.extract(r'_(\d+m)')
    long['datetime'] = pd.to_datetime(long['date']) + pd.to_timedelta(long['hour'], unit='h')
    result = long.pivot_table(index='datetime', columns='window', values='value')
    result.columns.name = None
    return result[['6m', '12m', '18m', '24m']]
