import numpy as np
from remodels.transformers.StandardizingScaler import StandardizingScaler
from scipy.stats import norm
import pandas as pd

def mad_median(arr):
    """
    Compute the Median Absolute Deviation of an array
    """
    median = np.median(arr)
    dev = np.abs(arr-median)
    return np.median(dev)

class MedianStandardizingScaler(StandardizingScaler):
    """
    Extend the StandardizingSclaer class to overwrite mean-based mad-function with mad_median
    """
    def _compute_center_scale(self, data):
        if self.method == "median":
            center = np.median(data)
            scale = mad_median(data) / norm.ppf(0.75)
        else:
            center = np.mean(data)
            scale = np.std(data)
        # prevent zero divison 
        scale = 1 if scale == 0 else scale
        return center, scale
    
